import java.util.*;
public class App {
    public static void main(String[] args){
        // this is simple for loop to print numbers from 0 to 5.
        // counter is initialised to 0, and it will go till 5 (included). counter++ increments the counter by 1
        for (int counter=0; counter<=5; counter++){
            System.out.println(counter);
        }
    }
}
